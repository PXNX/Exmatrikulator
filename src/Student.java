public class Student {

    private int matnr = 0;
    private String name = "";
    private double grade = 0.0;
    private boolean exmat = false;

    public int getMatnr() {
        return matnr;
    }

    public void setMatnr(int matnr) {
        this.matnr = matnr;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getGrade() {
        return grade;
    }

    public void setGrade(double grade) {
        this.grade = grade;
    }

    public boolean isExmat() {
        return exmat;
    }

    public void setExmat(boolean exmat) {
        this.exmat = exmat;
    }

}
